# list methods

list = ["siri","mohan","adi","gautham","mani",]
print(list)

list.sort() #arranges the list 
print(list)

list.reverse() #reverse's the list
print(list) 

list.append("naveen") #add's naveen at the end of the list
print(list)

list.insert(3,"nithin") #add's nithin at index 3
print(list) #used to insert in the list using index

list.pop(4) #it delete's the 4th index("gautham") from the list
print(list)

list.remove("mohan") #removes "mohan" from the list
print(list)